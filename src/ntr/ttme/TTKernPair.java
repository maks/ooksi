package ntr.ttme;

public class TTKernPair
{
    short left;
    short right;
    short value;

    TTKernPair()
    {
        super();
    }

    public short getLeft()
    {
        return left;
    }

    public short getRight()
    {
        return right;
    }

    public short getValue()
    {
        return value;
    }

} // END TTKernPair
