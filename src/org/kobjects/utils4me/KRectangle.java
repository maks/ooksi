package org.kobjects.utils4me;


/**
 * Simple 2D rectangle container class to hold x, y, fullWidth and height data.
 * @author Stefan Kerkewitz
 */
public final class KRectangle {

	/** The x coordinate of this rectangle */
	public int x;

	/** The y coordinate of this rectangle */
	public int y;

	/** The fullWidth coordinate of this rectangle */
	public int w;

	/** The height of this rectangle */
	public int h;
}
